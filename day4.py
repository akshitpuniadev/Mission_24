# Day 9: Typecasting Example
a = "10" # String
b = "20" # String
print("Without Typecasting:", a + b) # Output: 1020

# Explicit Typecasting
sum_val = int(a) + int(b)
print("With Typecasting:", sum_val) # Output: 30

